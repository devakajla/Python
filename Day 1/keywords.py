# ==========================================================
#                PYTHON RESERVED KEYWORDS
# ==========================================================
#
# False      -> Boolean False value
# None       -> Represents no value (null)
# True       -> Boolean True value
#
# and        -> Logical AND operator
# as         -> Creates an alias
# assert     -> Checks a condition (debugging)
#
# async      -> Defines asynchronous functions
# await      -> Waits for an async operation
#
# break      -> Exits the current loop
# case       -> Used with match statement
# class      -> Defines a class
# continue   -> Skips the current loop iteration
#
# def        -> Defines a function
# del        -> Deletes an object/reference
# elif       -> Else If condition
# else       -> Executes if previous condition is False
# except     -> Handles exceptions
# finally    -> Always executes after try/except
#
# for        -> Creates a loop
# from       -> Imports specific objects from a module
# global     -> Declares a global variable
# if         -> Conditional statement
# import     -> Imports a module or library
# in         -> Membership operator
#
# is         -> Identity operator
# lambda     -> Anonymous function
# match      -> Pattern matching (Python 3.10+)
# nonlocal   -> Refers to an outer function variable
# not        -> Logical NOT operator
# or         -> Logical OR operator
#
# pass       -> Empty placeholder statement
# raise      -> Raises an exception
# return     -> Returns a value from a function
# try        -> Starts exception handling
# while      -> While loop
# with       -> Context manager
# yield      -> Returns a value from a generator
#
# NOTE:
# Keywords are reserved words and cannot be used as
# variable names, function names, or class names.
# ==========================================================